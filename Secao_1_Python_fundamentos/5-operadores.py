num1 = int(input("Digite o primeiro número: "))
num2 = int(input("Digite o segundo número: "))

# Operadores aritméticos
print("Soma: ", num1 + num2)
print("Subtração: ", num1 - num2)
print("Multiplicação: ", num1 * num2)
print("Divisão: ", num1 / num2)
print("Resto da divisão: ", num1 % num2)
print("Potência: ", num1 ** num2)

# Comparação
print("> Maior: ", num1 > num2)
print("< Menor: ", num1 < num2)
print(">= Maior ou igual: ", num1 >= num2)
print("<= Menor ou igual: ", num1 <= num2)
print("== Igual: ", num1 == num2)
print("!= Diferente: ", num1 != num2)

# Atribuição
num1 += 1
num2 -= 1
num1 *= 2
num2 /= 2

# Operadores logicos
print("E: ", num1 and num2)
print("OU: ", num1 or num2)
print("Negação: ", not num1)

